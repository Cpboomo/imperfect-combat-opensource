// Helper: replace all byte sequences
function replaceAll(findArr, replArr) {
  let f = Buffer.from(findArr), r = Buffer.from(replArr), c = 0, p = 0;
  while (true) {
    let idx = b.indexOf(f, p);
    if (idx < 0) break;
    b = Buffer.concat([b.subarray(0, idx), r, b.subarray(idx + f.length)]);
    c++; p = idx + r.length;
  }
  return c;
}

