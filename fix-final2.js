function replace(fArr, rArr) {
  let f = Buffer.from(fArr), r = Buffer.from(rArr), n = 0, p = 0;
  while (true) {
    let i = b.indexOf(f, p); if (i < 0) break;
    b = Buffer.concat([b.subarray(0,i), r, b.subarray(i+f.length)]);
    n++; p = i + r.length;
  }
  return n;
}

